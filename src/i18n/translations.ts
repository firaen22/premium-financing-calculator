function traditionalToSimplified(text: string): string {
  let result = '';
  for (const char of text) {
    result += TRAD_TO_SIMP.get(char) || char;
  }
  return result;
}

function convertTranslations(obj: any): any {
  if (typeof obj === 'string') {
    return traditionalToSimplified(obj);
  }
  if (typeof obj === 'object' && obj !== null) {
    const result: any = {};
    for (const key of Object.keys(obj)) {
      result[key] = convertTranslations(obj[key]);
    }
    return result;
  }
  return obj;
}

